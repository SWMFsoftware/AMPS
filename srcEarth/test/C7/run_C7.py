#!/usr/bin/env python3
"""C7 — T96/T05 model-to-model vertical-cutoff validation.

C7 compares AMPS with cutoff values generated by independent geomagnetic
backtracing systems.  Two reference sources are supported:

  GEOMAGSPHERE
      T96/T05 vertical-cutoff calculator results.  GeoMagSphere reports lower,
      upper, and an "effective" cutoff defined as the midpoint of the lower and
      upper limits.  C7 therefore compares that reported effective value with
      ``0.5*(Rc_lower+Rc_upper)`` from AMPS, while retaining the AMPS penumbra-
      integral effective cutoff as an additional diagnostic.

  IZMIRAN
      Cutoff2050 exported results.  The input CSV must state whether the reported
      effective cutoff is a lower/upper midpoint or a penumbra integral.  C7 does
      not silently guess this convention.

The authoritative web calculators do not expose a stable bulk API.  This runner
therefore separates reference acquisition from model execution:

  1. ``--prepare-reference-requests`` writes a calculator request manifest.
  2. The user submits/export cases from the external calculator.
  3. ``tools/import_reference.py`` converts exported rows into the strict C7 CSV.
  4. ``run_C7.py --reference FILE`` executes AMPS and evaluates the comparison.

A complete 28-point IZMIRAN T96 reference is bundled with this test, so the
normal validation and run commands do not require ``--reference``. Matplotlib
is optional: when available, the runner writes a compact reference-versus-AMPS
comparison plot; a missing Matplotlib installation never changes pass/fail.

The default fixed locations follow validation.docx: 500-km geodetic altitude,
latitudes -60,-45,-30,0,30,45,60 degrees and east longitudes 0,90,180,270
degrees.  IZMIRAN request files express the equivalent signed longitudes
0,90,180,-90 degrees to satisfy its accepted [-180,180] input range. AMPS
evaluates a 15-by-90-degree shell and the runner extracts only
those 28 locations, avoiding any ambiguity in geographic-to-GSM conversion.
"""

