def reference_row_to_dict(row: "ReferenceRow") -> Dict[str, object]:
    """Return one reference row in the portable, strict C7 CSV schema.

    ``dataclasses.asdict`` would emit the six history integrals as a single
    tuple-valued ``w`` field.  The external-reference format deliberately uses
    W1..W6 as independent numeric columns because those values are part of the
    physical T05 state and must remain inspectable and reusable outside Python.
    """

    result: Dict[str, object] = {
        "request_id": row.request_id,
        "reference_source": row.reference_source,
        "model": row.model,
        "epoch_utc": row.epoch_utc,
        "latitude_deg": row.latitude_deg,
        "longitude_deg_east": row.longitude_deg_east,
        "altitude_km": row.altitude_km,
        "rc_lower_gv": row.rc_lower_gv,
        "rc_upper_gv": row.rc_upper_gv,
        "rc_effective_gv": "" if row.rc_effective_gv is None else row.rc_effective_gv,
        "effective_definition": row.effective_definition,
        "rigidity_step_gv": "" if row.rigidity_step_gv is None else row.rigidity_step_gv,
        "dst_nT": row.dst_nT,
        "pdyn_nPa": row.pdyn_nPa,
        "imf_bx_nT": row.imf_bx_nT,
        "imf_by_nT": row.imf_by_nT,
        "imf_bz_nT": row.imf_bz_nT,
        "sw_vx_kms": row.sw_vx_kms,
        "sw_n_cm3": row.sw_n_cm3,
        "reference_version": row.reference_version,
        "citation": row.citation,
        "notes": row.notes,
    }
    for index, value in enumerate(row.w, 1):
        result["w%d" % index] = value
    return {field: result[field] for field in REFERENCE_FIELDS}


@dataclass(frozen=True)
class ModelRow:
    latitude_deg: float
    longitude_deg_east: float
    rc_lower_gv: float
    rc_effective_integral_gv: float
