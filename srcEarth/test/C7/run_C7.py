def main(argv: Optional[Sequence[str]] = None) -> int:
    args = parse_args(argv)
    if args.np < 1 or args.nt < 1:
        raise SystemExit("-np and -nt must be positive")
    if args.dynamic_chunk < 0:
        raise SystemExit("--dynamic-chunk must be non-negative")
    if args.cutoff_scan_n < 2:
        raise SystemExit("--cutoff-scan-n must be at least 2")
    if not (args.rigidity_max_gv > args.rigidity_min_gv >= 0.0):
        raise SystemExit("require 0 <= rigidity-min-gv < rigidity-max-gv")
    if args.domain_half_size_re <= 1.1:
        raise SystemExit("--domain-half-size-re must exceed the 500-km shell radius")
    script_dir = Path(__file__).resolve().parent
    launch_dir = Path.cwd().resolve()
    if args.prepare_reference_requests:
        output = Path(args.request_output_dir).expanduser()
        if not output.is_absolute(): output = launch_dir / output
        prepare_requests(script_dir, output)
        print("C7 reference request package: %s" % output)
        return 0
    if not args.reference:
        bundled_reference = script_dir / BUNDLED_REFERENCE_NAME
        if not bundled_reference.is_file():
            print(
                "C7 has no --reference argument and the bundled reference is missing: %s"
                % bundled_reference,
                file=sys.stderr,
            )
            return 2
        args.reference = [str(bundled_reference)]
        print("C7 using bundled IZMIRAN reference: %s" % bundled_reference)
    paths = [Path(p).expanduser().resolve() for p in args.reference]
    try:
        references = load_references(paths)
    except Exception as exc:
        print("C7 reference validation failed: %s" % exc, file=sys.stderr)
        return 2
    requested_models = {canonical_model(v) for v in args.models.split(",") if v.strip()}
    references = [r for r in references if r.model in requested_models and (args.source == "ALL" or r.reference_source == args.source)]
    if not references:
        print("No C7 rows remain after --source/--models filtering", file=sys.stderr)
        return 2
    groups: Dict[Tuple[object, ...], List[ReferenceRow]] = {}
    for row in references:
        groups.setdefault(group_key(row), []).append(row)
    for group in groups.values():
        coords = {coordinate_key(r.latitude_deg, r.longitude_deg_east) for r in group}
        expected = {coordinate_key(lat, lon) for lat in TARGET_LATS for lon in TARGET_LONS}
        if coords != expected:
            missing = sorted(expected - coords)
            extra = sorted(coords - expected)
            raise SystemExit("C7 group must contain the planned 28 points; missing=%s extra=%s" % (missing, extra))
    inventory = {"files": [str(p) for p in paths], "rows": len(references), "groups": len(groups), "sources": sorted({r.reference_source for r in references}), "models": sorted({r.model for r in references})}
    if args.validate_references:
        print(json.dumps(inventory, indent=2)); return 0
    work_root = Path(args.workdir).expanduser()
    if not work_root.is_absolute(): work_root = launch_dir / work_root
    if not args.skip_run and not args.keep and work_root.exists(): shutil.rmtree(work_root)
    work_root.mkdir(parents=True, exist_ok=True)
    (work_root / "C7_reference_inventory.json").write_text(json.dumps(inventory, indent=2) + "\n")
    amps = Path(args.amps)
    if not amps.is_absolute(): amps = launch_dir / amps
    all_metrics: List[Metrics] = []
    all_pass = True
    for group_index, group in enumerate(groups.values(), 1):
        first = group[0]
        label = "%s_%s_%s_%02d" % (first.reference_source.lower(), first.model.lower(), re.sub(r"[^0-9]", "", first.epoch_utc)[:12], group_index)
        for solver in selected_solvers(args.solver):
            case_dir = work_root / label / solver.lower()
            case_dir.mkdir(parents=True, exist_ok=True)
            template = script_dir / ("AMPS_PARAM_C7_gridless.in" if solver == "GRIDLESS" else "AMPS_PARAM_C7_mode3d.in")
            render_input(template, case_dir / "AMPS_PARAM_C7.in", group, args, solver)
            write_rows(case_dir / "reference_C7_selected.csv", [reference_row_to_dict(r) for r in group])
            command = command_for(args, amps, solver)
            print("C7 %s/%s command:\n  %s" % (label, solver.lower(), " ".join(command)))
            if not args.skip_run and not args.dry_run:
                rc = run_process(command, case_dir, case_dir / "C7_amps.log")
                if rc != 0: return rc
            if args.dry_run: continue
            output = case_dir / ("cutoff_gridless_shells_penumbra.dat" if solver == "GRIDLESS" else "cutoff_3d_shells_penumbra.dat")
            try:
                model_rows = parse_tecplot(output)
                details, metrics = compare(group, model_rows, args, solver)
            except Exception as exc:
                print("C7 analysis failed for %s/%s: %s" % (label, solver, exc), file=sys.stderr)
                return 2
            write_rows(case_dir / "C7_comparison.csv", details)
            (case_dir / "C7_result.json").write_text(json.dumps(asdict(metrics), indent=2) + "\n")
            make_case_plot(
                details,
                case_dir / "C7_comparison.png",
                "%s %s %s %s %s" % (
                    TEST_ID,
                    solver,
                    first.reference_source,
                    first.model,
                    first.epoch_utc,
                ),
            )
            all_metrics.append(metrics); all_pass = all_pass and metrics.passed
            print("C7 %s/%s: valid=%d/%d pass_fraction=%.3f lower_RMSE=%.3f upper_RMSE=%.3f max_RMSE=%.3f -> %s" % (label, solver.lower(), metrics.n_valid, metrics.n_reference, metrics.point_pass_fraction, metrics.lower_rmse_gv, metrics.upper_rmse_gv, metrics.maximum_primary_rmse_gv, "PASS" if metrics.passed else "FAIL"))
    (work_root / "C7_summary.json").write_text(json.dumps([asdict(m) for m in all_metrics], indent=2) + "\n")
    if args.dry_run:
        print("C7 dry run complete: %s" % work_root); return 0
    print("C7 overall: %s" % ("PASS" if all_pass else "FAIL"))
    print("Results: %s" % work_root)
    return 0 if all_pass else 1


if __name__ == "__main__":
    raise SystemExit(main())
