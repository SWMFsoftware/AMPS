def normalize_lon(value: float) -> float:
    """Normalize signed or unsigned east longitude to the C7 [0,360) convention."""
    value = float(value) % 360.0
    return 0.0 if abs(value) < 1.0e-9 or abs(value - 360.0) < 1.0e-9 else value


def signed_lon(value: float) -> float:
    """Return equivalent east longitude in the calculator range [-180,180]."""
    value = normalize_lon(value)
    return value - 360.0 if value > 180.0 else value


def canonical_model(value: str) -> str:
    u = str(value).strip().upper()
    aliases = {"TS96": "T96", "T96S": "T96", "TS05": "T05", "T05S": "T05", "T04S": "T05"}
    u = aliases.get(u, u)
    if u not in MODELS:
        raise ValueError("unsupported model %r; expected T96 or T05" % value)
    return u


def canonical_source(value: str) -> str:
    u = str(value).strip().upper()
    if u not in SOURCES:
        raise ValueError("unsupported reference source %r" % value)
    return u


def optional_float(text: object) -> Optional[float]:
    value = "" if text is None else str(text).strip()
    return None if value == "" else float(value)


def required_float(raw: Mapping[str, str], key: str, path: Path, line: int) -> float:
    value = str(raw.get(key, "")).strip()
    if not value:
        raise RuntimeError("%s:%d missing required numeric field %s" % (path, line, key))
    try:
        result = float(value)
    except ValueError as exc:
        raise RuntimeError("%s:%d invalid %s=%r" % (path, line, key, value)) from exc
    if not math.isfinite(result):
        raise RuntimeError("%s:%d non-finite %s" % (path, line, key))
    return result


def load_references(paths: Sequence[Path]) -> List[ReferenceRow]:
    required = {
        "request_id", "reference_source", "model", "epoch_utc",
        "latitude_deg", "longitude_deg_east", "altitude_km",
        "rc_lower_gv", "rc_upper_gv", "effective_definition",
        "dst_nT", "pdyn_nPa", "imf_bx_nT", "imf_by_nT", "imf_bz_nT",
        "sw_vx_kms", "sw_n_cm3", "w1", "w2", "w3", "w4", "w5", "w6",
    }
    result: List[ReferenceRow] = []
    seen = set()
    for path in paths:
        with path.open(newline="") as stream:
            reader = csv.DictReader(stream)
            missing = sorted(required - set(reader.fieldnames or []))
            if missing:
                raise RuntimeError("%s missing columns: %s" % (path, ", ".join(missing)))
            for line, raw in enumerate(reader, 2):
                source = canonical_source(raw["reference_source"])
                model = canonical_model(raw["model"])
                if (source, model) not in SUPPORTED_REFERENCE_PAIRS:
                    raise RuntimeError(
                        "%s:%d unsupported C7 source/model pair %s/%s; "
                        "GeoMagSphere supplies T96 and T05, while IZMIRAN is "
                        "used only as the secondary T96 check"
                        % (path, line, source, model)
                    )
                definition = str(raw.get("effective_definition", "")).strip().upper()
                if definition not in {"MIDPOINT", "PENUMBRA_INTEGRAL", "REPORTED_UNKNOWN", "NONE"}:
                    raise RuntimeError("%s:%d invalid effective_definition=%r" % (path, line, definition))
                row = ReferenceRow(
                    request_id=str(raw["request_id"]).strip(),
                    reference_source=source,
                    model=model,
                    epoch_utc=str(raw["epoch_utc"]).strip(),
                    latitude_deg=required_float(raw, "latitude_deg", path, line),
                    longitude_deg_east=normalize_lon(required_float(raw, "longitude_deg_east", path, line)),
                    altitude_km=required_float(raw, "altitude_km", path, line),
                    rc_lower_gv=required_float(raw, "rc_lower_gv", path, line),
                    rc_upper_gv=required_float(raw, "rc_upper_gv", path, line),
                    rc_effective_gv=optional_float(raw.get("rc_effective_gv")),
                    effective_definition=definition,
                    rigidity_step_gv=optional_float(raw.get("rigidity_step_gv")),
                    dst_nT=required_float(raw, "dst_nT", path, line),
                    pdyn_nPa=required_float(raw, "pdyn_nPa", path, line),
                    imf_bx_nT=required_float(raw, "imf_bx_nT", path, line),
                    imf_by_nT=required_float(raw, "imf_by_nT", path, line),
                    imf_bz_nT=required_float(raw, "imf_bz_nT", path, line),
                    sw_vx_kms=required_float(raw, "sw_vx_kms", path, line),
                    sw_n_cm3=required_float(raw, "sw_n_cm3", path, line),
                    w=tuple(required_float(raw, "w%d" % i, path, line) for i in range(1, 7)),
                    reference_version=str(raw.get("reference_version", "")).strip(),
                    citation=str(raw.get("citation", "")).strip(),
                    notes=str(raw.get("notes", "")).strip(),
                )
                if row.rc_upper_gv < row.rc_lower_gv:
                    raise RuntimeError("%s:%d upper cutoff is below lower cutoff" % (path, line))
                if source == "GEOMAGSPHERE":
                    # GeoMagSphere's public calculator documentation defines the
                    # reported effective cutoff as the midpoint of its lower and
                    # upper cutoffs.  Requiring that declaration here prevents a
                    # superficially good comparison between unlike quantities.
                    if definition != "MIDPOINT":
                        raise RuntimeError("%s:%d GeoMagSphere rows must declare MIDPOINT" % (path, line))
                    if row.rc_effective_gv is None:
                        raise RuntimeError("%s:%d GeoMagSphere row is missing rc_effective_gv" % (path, line))
                    midpoint = 0.5 * (row.rc_lower_gv + row.rc_upper_gv)
                    tolerance = max(0.051, 0.6 * (row.rigidity_step_gv or 0.1))
                    if abs(row.rc_effective_gv - midpoint) > tolerance:
                        raise RuntimeError(
                            "%s:%d GeoMagSphere effective cutoff %.6g is inconsistent with "
                            "the documented midpoint %.6g" %
                            (path, line, row.rc_effective_gv, midpoint)
                        )
                key = (source, model, row.epoch_utc, round(row.latitude_deg, 7), round(row.longitude_deg_east, 7))
                if key in seen:
                    raise RuntimeError("duplicate C7 reference row: %s" % (key,))
                seen.add(key)
                result.append(row)
    if not result:
        raise RuntimeError("no populated C7 reference rows were loaded")
    return result


def group_key(row: ReferenceRow) -> Tuple[object, ...]:
    return (
        row.reference_source, row.model, row.epoch_utc, row.altitude_km,
        row.dst_nT, row.pdyn_nPa, row.imf_bx_nT, row.imf_by_nT, row.imf_bz_nT,
        row.sw_vx_kms, row.sw_n_cm3, *row.w,
    )


def kinetic_energy_mev(rigidity_gv: float) -> float:
    rest_mev = PROTON_MASS_AMU * AMU_KG * C_LIGHT_M_S ** 2 / MEV_J
    p_mev_c = float(rigidity_gv) * 1000.0
    return math.sqrt(p_mev_c * p_mev_c + rest_mev * rest_mev) - rest_mev


def replace_directive(text: str, directive: str, value: object) -> str:
    lines = text.splitlines()
    output: List[str] = []
    count = 0
    for line in lines:
        stripped = line.lstrip()
        if not stripped or stripped.startswith(("!", "#")):
            output.append(line)
            continue
        tokens = stripped.split(None, 1)
        if tokens[0] != directive:
            output.append(line)
            continue
        count += 1
        comment = ""
        if len(tokens) > 1 and "!" in tokens[1]:
            comment = "  !" + tokens[1].split("!", 1)[1]
        output.append("%-32s%s%s" % (directive, value, comment))
    if count != 1:
        raise RuntimeError("expected one %s directive, found %d" % (directive, count))
    return "\n".join(output) + "\n"


