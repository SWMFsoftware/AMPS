  static const double minAlt_EnergyGrid=80.0E3,maxAlt_EnergyGrid=400.0E3,dAlt_EnergyGrid=5.0E3;

  struct cEnergyDistributionData {
    double ALT;
    double Ti;
    double Te;
    double EnergyDistribution[300];
    double Emin;
    double Emax;
    double dE;
    double fmax;
  };


  #include "EnergyGridFox.h"

  inline double GetInjectionEnergy(double *x) {
    double alt=sqrt(x[0]*x[0]+x[1]*x[1]+x[2]*x[2])-_MARS__RADIUS_;
    int iLevel,i;
    double p,e,a,b;

    iLevel=(int)(alt/dAlt_EnergyGrid);
    if (iLevel<0) iLevel=0;
    if (iLevel>=65) iLevel=64;

    do {
      e=EnergyDistributionData[iLevel].Emin+rnd()*(EnergyDistributionData[iLevel].Emax-EnergyDistributionData[iLevel].Emin);
      i=(int)((e-EnergyDistributionData[iLevel].Emin)/EnergyDistributionData[iLevel].dE);

      a=((e-EnergyDistributionData[iLevel].Emin)- i*EnergyDistributionData[iLevel].dE)/EnergyDistributionData[iLevel].dE;
      b=1.0-a;

      p=(a*EnergyDistributionData[iLevel].EnergyDistribution[i]+b*EnergyDistributionData[iLevel].EnergyDistribution[i+1])/EnergyDistributionData[iLevel].fmax;
    }
    while (p<rnd());

    return e;
  }
